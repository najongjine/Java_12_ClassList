package com.biz.list.model;

public class AddressVO {
	private String strName;
	private String strAddress;
	private String strTel;
	private String strCellph;
	private int intPoint;
	
	public String getStrName() {
		return strName;
	}
	public void setStrName(String strName) {
		this.strName = strName;
	}
	public String getStrAddress() {
		return strAddress;
	}
	public void setStrAddress(String strAddress) {
		this.strAddress = strAddress;
	}
	public String getStrTel() {
		return strTel;
	}
	public void setStrTel(String strTel) {
		this.strTel = strTel;
	}
	public String getStrCellph() {
		return strCellph;
	}
	public void setStrCellph(String strCellph) {
		this.strCellph = strCellph;
	}
	public int getIntPoint() {
		return intPoint;
	}
	public void setIntPoint(int intPoint) {
		this.intPoint = intPoint;
	}//end
	

}
